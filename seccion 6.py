"""
la conversión en Python se realiza mediante funciones constructoras:

int() - construye un número entero a partir de un literal entero, un literalflotante
(eliminando todos los decimales) o un literal de cadena (siempre que la cadena represente un número entero)

float() - construye un número flotante a partir de un literal entero, un literal
flotante o un literal de cadena (siempre que la cadena represente un flotante o un entero)

str() - construye una cadena a partir de una amplia variedad de tipos de
datos, incluidas cadenas, literales enteros y literales flotantes
"""
x = int(1)   # x podria ser 1
y = int(2.8) # y podria ser 2
